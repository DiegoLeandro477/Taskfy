    private void btFinishMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btFinishMouseClicked
        try {
            if (update){
                controller.update(task, jtName.getText(), jtDescription.getText(), sdf.parse(jfDeadline.getText()), jtNodes.getText());
            }else
                controller.createTask(jtName.getText(), jtDescription.getText(), jfDeadline.getText(), jtNodes.getText());
        } catch (Exception e) {
                Logger.getLogger(AddTaskScreen.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_btFinishMouseClicked

