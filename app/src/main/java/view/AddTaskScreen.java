    public void edit(Task task) {
        this.task = task;
        jtName.setText(task.getName());
        jtDescription.setText(task.getDescription());
        jfDeadline.setText(sdf.format(task.getDeadline()));
        jtNodes.setText(task.getNodes());
        update = true;
        this.index = task.getId();
    }
    
